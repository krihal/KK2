void st7565DrawLine( uint8_t x1, uint8_t y1, uint8_t x2, uint8_t y2 )
{
	int16_t dx, dy, stepx=1, stepy=1, fraction;

	dy = y2 - y1;
	dx = x2 - x1;

	if ( dy < 0 ) {
		dy    = -dy;
		stepy = -1;
	}

	if ( dx < 0 ) {
		dx    = -dx;
		stepx = -1;
	}

	dx <<= 1;
	dy <<= 1;

	st7565DrawPixel( x1, y1 );

	if ( dx > dy )
	{
		fraction = dy - (dx >> 1);
		while ( x1 != x2 )
		{
			if ( fraction >= 0 )
			{
				y1 += stepy;
				fraction -= dx;
			}
			x1 += stepx;
			fraction += dy;
			st7565DrawPixel( x1, y1 );
		}
	} else {
		fraction = dx - (dy >> 1);
		while ( y1 != y2 )
		{
			if ( fraction >= 0 )
			{
				x1 += stepx;
				fraction -= dy;
			}
			y1 += stepy;
			fraction += dx;
			st7565DrawPixel( x1, y1 );
		}
	}
}
